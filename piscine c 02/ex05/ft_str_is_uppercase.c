int			ft_str_is_uppercase(char *str)
{
	unsigned int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while ((str[i] >= 'A' && str[i] <= 'Z') || str[i] == '\0')
	{
		if (str[i] == '\0')
			return (1);
		i++;
	}
	return (0);
}
