int				ft_str_is_printable(char *str)
{
	unsigned int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while ((str[i] >= 32 && str[i] <= 127) || str[i] == '\0')
	{
		if (str[i] == '\0')
			return (1);
		i++;
	}
	return (0);
}
