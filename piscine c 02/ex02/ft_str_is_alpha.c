int		ft_str_is_alpha(char *str)
{
	unsigned int i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while ((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z') || str[i] == '\0')
	{
		if (str[i] == '\0')
			return (1);
		i++;
	}
	return (0);
}
