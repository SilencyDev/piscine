#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;

	if (!(dest = malloc(sizeof(src) + 1)))
		return (0);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
